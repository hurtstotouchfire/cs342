
public class Node {
    protected Node next;
    protected Node prev;
    
    public Node getNext() {
    return next;
    }
    public void setNext(Node next) {
    this.next = next;
    }
    public Node getPrev() {
    return prev;
    }
    public void setPrev(Node prev) {
    this.prev = prev;
    }
    
}
